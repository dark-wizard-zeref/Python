# Inbuilt in python: List, Tuple, Dictionary, Set
# Customized: Stack, Queue, Linked List, Trees, Graphs, etc.

# List
a = [1,2,3,5,9,20]

# Tuple
b = (1,2,3,5,9,20)

# Set: Unordered
c = {1,2,3,5,9,20}

